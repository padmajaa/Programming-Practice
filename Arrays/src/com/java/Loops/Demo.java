package com.java.Loops;

public class Demo {
	
	   
	      public static void main (String[] args) {
		} {
	      
	   	 String[] name = {"John Doe", "Mary Jane", "Peter Pan"};
	    
	         /* ----------------------------------------------
		    Print the String array name
		    ---------------------------------------------- */
	   	 for ( int i = 0; i < name.length; i++ )
	   	 {
	   	    System.out.println( name[i] );
	   	 }
	    
	      }
	   
}

